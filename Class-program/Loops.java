
public class Loops{
public static void main(String[] args){

//for loops
for ( int i = 0 ; i<10; i++){
System.out.println("i = " +i);

}


// while loops

int j = 10;
while(j>0){
System.out.println("j :" +j);
j--;

}

int k = 10;

// do while loops

do {
    System.out.println("j : " + j);
    k--;
} while (k > 0);

 }
 
 
}






